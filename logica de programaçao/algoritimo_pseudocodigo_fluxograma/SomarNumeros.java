// Faça um algoritmo para somar três números e multiplicar o resultado pelo segundo número.

package algoritimo_pseudocodigo_fluxograma;

public class SomarNumeros {

	public static void main(String[] args) {

		int n1 = 5; 
		int n2 = 5;
		int n3 = 5;
		
		double soma = (n1 + n2 + n3);
		double mult = soma * n2;
		System.out.println(mult);
		
	}

}
